const std::string COMPUTE_SHADER_STR = "#version 450 core\n"
                                       "layout (local_size_x = 1, local_size_y = 1, local_size_z = 1) in;\n"
                                       "layout(rgba32f, set = 0, binding = 0) uniform image2D imgOutput;\n"
                                       "void main() {\n"
                                       "    vec4 value = vec4(0.0, 0.0, 0.0, 1.0);\n"
                                       "    ivec2 texelCoord = ivec2(gl_GlobalInvocationID.xy);\n"
                                       "    value.x = float(texelCoord.x)/(gl_NumWorkGroups.x);\n"
                                       "    value.y = float(texelCoord.y)/(gl_NumWorkGroups.y);	\n"
                                       "    imageStore(imgOutput, texelCoord, value);\n"
                                       "}\n";

const std::string VERT_SHADER_STR = "#version 450 core\n"
                                    "layout (set = 0, binding = 0) uniform bufferVals {\n"
                                    "    float value;\n"
                                    "} myBufferVals;\n"
                                    "layout (location = 0) out float outValue;\n"
                                    "layout (location = 1) out vec2 uv;\n"
                                    "void main() {\n"
                                    "   uv = vec2((gl_VertexIndex << 1) & 2, gl_VertexIndex & 2);\n"
                                    "   gl_Position = vec4(uv* 2.0f - 1.0f, 0.0f, 1.0f);\n"
                                    "   outValue = myBufferVals.value;\n"
                                    "}\n";

const std::string FRAG_SHADER_STR = "#version 450 core\n"
                                    "layout (set = 0, binding = 1) uniform texture2D myTexture;\n"
                                    "layout (set = 0, binding = 2) uniform sampler mySampler;\n"
                                    "layout (location = 0) in float inValue;\n"
                                    "layout (location = 1) in vec2 uv;\n"
                                    "layout (location = 0) out vec4 outColor;\n"
                                    "void main() {\n"
                                    "   vec4 color =texture(sampler2D(myTexture, mySampler), uv, 0);\n"
                                    "   outColor = vec4(color.xyz,1);\n"
                                    "}\n";

